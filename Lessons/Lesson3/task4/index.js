/* оператор || (OR) */
false || true;
false || true || false;
false || false || false;

console.log("false || true: ", false || true);
console.log("false || true || false: ", false || true || false);
console.log("false || false || false: ", false || false || false);

/* оператор && (AND) */
false && true;
false && true && false;
true && true && true;

console.log("false && true: ", false && true);
console.log("false && true && false: ", false && true && false);
console.log("true && true && true: ", true && true && true);
